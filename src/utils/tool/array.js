export function chunk(arr, size = 1) { const res = []; for (let i = 0; i < arr.length; i += size) res.push(arr.slice(i, i + size)); return res; }

export function compact(arr) { return arr.filter(Boolean); }

export function shuffle(arr) { const clone = [...arr]; for (let i = clone.length - 1; i > 0; i--) { const j = Math.floor(Math.random() * (i + 1)); [clone[i], clone[j]] = [clone[j], clone[i]]; } return clone; }

export function sample(arr) { return arr[Math.floor(Math.random() * arr.length)]; }

export function unique(arr) { return [...new Set(arr)]; }

